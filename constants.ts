export const generateNextGatePassId = (existingIds: string[]): string => {
  const prefix = "SP";
  const digitCount = 6;
  
  // Filter for IDs that follow our format SPXXXXXX
  const numericIds = existingIds
    .filter(id => id.startsWith(prefix) && id.length === prefix.length + digitCount)
    .map(id => parseInt(id.substring(prefix.length), 10))
    .filter(num => !isNaN(num));

  // Find the highest number or start at 0
  const lastNumber = numericIds.length > 0 ? Math.max(...numericIds) : 0;
  const nextNumber = lastNumber + 1;
  
  // Pad with leading zeros to maintain 6 digits
  return prefix + nextNumber.toString().padStart(digitCount, '0');
};

export const generateUniqueApplicationNo = (existingAppNos: string[]): string => {
  const chars = '0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ';
  let result = '';
  const length = 6;
  
  const generate = () => {
    let str = '';
    for (let i = 0; i < length; i++) {
      str += chars.charAt(Math.floor(Math.random() * chars.length));
    }
    return str;
  };

  result = generate();
  // Ensure uniqueness (in memory check)
  while (existingAppNos.includes(result)) {
    result = generate();
  }
  return result;
};

export const calculateToDate = (fromDate: string, days: number): string => {
  if (!fromDate || isNaN(days)) return '';
  const date = new Date(fromDate);
  date.setDate(date.getDate() + days);
  return date.toISOString().split('T')[0];
};
